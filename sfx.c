void SFXPlayerThread(void *args)
{
	// pass argument
	xQueueHandle sfxEventQueue = (xQueueHandle) args;

	// sfx index
	uint8_t sfx_idx;

	while (1)
	{
		// block wait until main thread gives instruction
		xQueueReceive(sfxEventQueue, &sfx_idx, portMAX_DELAY);

		// don't play sound if device is busy
		if (ClassDBusy())
		{
			continue;
		}

		switch (sfx_idx)
		{
			case SFX_FIRE:
			{
				ClassDPlayADPCM(g_pucFireEffect, sizeof(g_pucFireEffect));
				break;
			}
			case SFX_WALL:
			{
				ClassDPlayADPCM(g_pucWallEffect, sizeof(g_pucWallEffect));
				break;
			}
			default:
				break;
		}

	}
}
