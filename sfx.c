void SFXPlayerThread(void *args)
{
	// pass argument
	xQueueHandle sfxEventQueue = (xQueueHandle) args;

	// sfx index
	uint8_t sfx_idx;

	while (1)
	{
		// block wait until main thread gives instruction
		xQueueReceive(sfxEventQueue, &sfx_idx, portMAX_DELAY);

		// if normal task, then check for any tune is playing
		if (sfx_idx & SFX_NORMAL)
		{
			// don't play sound if device is busy
			if (ClassDBusy())
			{
				continue;
			}
		}

		// if preempt task, then stop current tune and play new one
		else if (sfx_idx & SFX_PREEMPT)
		{
			// stop current tune
			ClassDStop();

			// wait until fully stopped
			while (ClassDBusy())
			{

			}
		}

		// ignore other cases
		else
		{
			continue;
		}

		switch (sfx_idx & 0x0f) 	// filter sfx flags
		{
			case SFX_FIRE:
			{
				ClassDPlayADPCM(sound_fire_weapon, sizeof(sound_fire_weapon));
				break;
			}
			case SFX_WALL:
			{
				ClassDPlayADPCM(sound_footsteps, sizeof(sound_footsteps));
				break;
			}
			default:
				break;
		}

	}
}
